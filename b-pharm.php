						                	<h4>Eligibilty for Admission</h4>
						                	<p>All categories of candidate except SC/ST community should have minimum of 40% aggregate marks in HSc with science subjects (Physics, Chemistry & Mathematics, or Physics, Chemistry & Biology or Physics, Chemistry, Botany & Zoology). Diploma in Pharmacy holders (from a PCI recognized institute) is eligible to pursue this course under section 12 of the Pharmacy Act. The curricula and syllabi for the program shall be prescribed by Pharmacy Council of India, New Delhi and The Tamil Nadu Dr. M.G.R Medical University, Chennai.</p>
											<p>Each semester shall consist of not less than 100 working days. The odd semesters shall be conducted from the month of June/July to November/December and the even semesters shall be conducted from December/January to May/June in every calendar year. Candidate is required to put in at least 80% attendance in individual courses considering theory and practical separately. The candidate shall complete the prescribed course satisfactorily to be eligible to appear for the respective examinations.</p>
